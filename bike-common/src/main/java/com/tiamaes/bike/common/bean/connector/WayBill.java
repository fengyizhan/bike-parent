package com.tiamaes.bike.common.bean.connector;

import java.io.Serializable;

/**
 * 电子运单上报
 * @author waibao001
 *
 */
public class WayBill implements Serializable{
	
	private static final long serialVersionUID = 1L;
	/**
	 * 电子运单长度
	 */
	private int wayBillLength;
	/**
	 * 电子运单长度
	 */
	private byte[] wayBillContent;
	
	public int getWayBillLength() {
		return wayBillLength;
	}
	public void setWayBillLength(int wayBillLength) {
		this.wayBillLength = wayBillLength;
	}
	public byte[] getWayBillContent() {
		return wayBillContent;
	}
	public void setWayBillContent(byte[] wayBillContent) {
		this.wayBillContent = wayBillContent;
	}
}
